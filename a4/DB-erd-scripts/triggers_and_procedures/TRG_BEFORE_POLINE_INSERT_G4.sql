CREATE TRIGGER TRG_BEFORE_POLINE_INSERT_G4
BEFORE
INSERT ON POLinesG4
FOR EACH ROW
BEGIN
	-- FOR EACH NEW PoLineG4 RECORD INSERT EVENT, DEDUCT THE SOLD QUANTITY FROM THE PART INVENTORY IN THE PartsG4 TABLE.
	UPDATE PartsG4 p SET p.qtyG4 = p.qtyG4 - NEW.qtyG4 WHERE p.partNoG4 = NEW.partNoG4;
END  //
DELIMITER ;
